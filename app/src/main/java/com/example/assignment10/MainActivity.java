public class MainActivity extends AppCompatActivity implements CitiesFragment.CitiesListener {

    final String TAG = "demo";


    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        EdgeToEdge.enable(this);
        setContentView(R.layout.activity_main);
        ViewCompat.setOnApplyWindowInsetsListener(findViewById(R.id.main), (v, insets) -> {
            Insets systemBars = insets.getInsets(WindowInsetsCompat.Type.systemBars());
            v.setPadding(systemBars.left, systemBars.top, systemBars.right, systemBars.bottom);
            return insets;
        });

        getSupportFragmentManager()
                .beginTransaction()
                .replace(R.id.main, new CitiesFragment(), "cities-fragment")
                .commit();



    }

    @Override
    public void onCitySelected(City city) {
        getSupportFragmentManager()
                .beginTransaction()
                .replace(R.id.main, ForecastFragment.newInstance(city), "forecast-detail-fragment")
                .addToBackStack(null)
                .commit();
    }
}